#Reducción de varianza
sim=10000
phi=c()
phi_f=c()
i=1
while(i<sim+1) {
U=rbinom(6,size=1,p=runif(6,0,1))
phi[i]=max(U[1]*U[2]*U[5],U[1]*U[4]*U[3]*U[2]*U[5],
        U[1]*U[4]*U[6],U[1]*U[2]*U[3]*U[6],U[1]*U[2]*U[3]
        *U[4]*U[6],U[1]*U[4]*U[3]*U[6])
phi_f[i]=max((1-U[1])*(1-U[2])*(1-U[5]),(1-U[1])*(1-U[4])
              *(1-U[3])*(1-U[2])*(1-U[5]),
              (1-U[1])*(1-U[4])*(1-U[6]),(1-U[1])*(1-U[2])
              *(1-U[3])*(1-U[6]),(1-U[1])*(1-U[2])*(1-U[3])
              *(1-U[4])*(1-U[6]),(1-U[1])*(1-U[4])*(1-U[3])
             *(1-U[6]))
i=i+1
}
var(phi/2)+var(phi_f/2)+(1/2)*cov(phi,phi_f)
cov(phi,phi_f)
